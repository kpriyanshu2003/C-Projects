// WAP to initiaalize an integer variable, and display it using all format specifier
#include <stdio.h>
int main()
{
	int a = 10;
	printf("Character : %c\n", a);
	printf("String : %s\n", a);
	printf("Decimal Integer: %d\n", a);
	printf("Floating Point : %f\n", a);
	printf("Double : %d", a);
	printf("Long Double : %lf\n", a);
	return 0;
}
